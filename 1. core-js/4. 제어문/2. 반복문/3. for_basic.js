// 5 ~ 0 카운트 다운
for (var m = 5; m >= 0; m--) {
    console.log(`${m}!!`);
}
console.log('======================');

// 1 ~ 10 까지의 총합
var total = 0; //총합을 저장할 변수
for (var k = 1; k <= 10; k++) {
    total += k;    
}
console.log(`총합: ${total}`);
console.log('======================');